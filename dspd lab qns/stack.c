#include<stdio.h>
#include<stdlib.h>
typedef struct Node_tag
{	int data;
	struct Node_tag *next;
}Node_type;

typedef struct stack_tag
{	Node_type *top;
}stack_type;

typedef enum{FAILURE,SUCCESS}status_code;

void Initialize(stack_type *stck_ptr)
{	stck_ptr->top=NULL;
}
Node_type* MakeNode(int d)
{	Node_type *nptr;
	nptr=(Node_type*)malloc(sizeof(Node_type));
	if(nptr!=NULL)
	{	nptr->data=d;
		nptr->next=NULL;
	}
	return nptr;
}

status_code Push(stack_type *sptr,int d)
{	Node_type *nptr;
	status_code ret_val=SUCCESS;
	nptr=MakeNode(d);
	if(nptr==NULL)
	{	ret_val=FAILURE;
	}
	else
	{	nptr->next=sptr->top;
		sptr->top=nptr;
	}
	return ret_val;
}

status_code Pop(stack_type *sptr,int *d)
{	Node_type *ptr;
	status_code sc=SUCCESS;
	if(sptr->top==NULL)
	{	sc=FAILURE;
	}
	else
	{	ptr=sptr->top;
		sptr->top=ptr->next;
		*d=ptr->data;
		free(ptr);
	}
	return sc;
}

void main()
{	stack_type stck;
	status_code sc;
	Initialize(&stck);
	
	int choice,x;
	char ch='y';
	while(ch=='y' || ch=='Y')
	{	printf("1.PUSH\n2.POP\n");
		scanf("%d",&choice);
		switch(choice)
		{	case 1:printf("ENTER THE NO TO BE PUSHED : ");
					scanf("%d",&x);
					sc=Push(&stck,x);
					if(sc==FAILURE)
						printf("CANNOT PUSH THE GIVEN NO ");
					else
						printf("THE NO IS PUSHED\n");
					break;
			case 2:sc=Pop(&stck,&x);
					if(sc==FAILURE)
						printf("CANNOT POP OUT OF AN EMPTY LIST\n");
					else
						printf("%d IS POPED \n",x);
					break;
			default:break;
		}
		printf("DO YOU WANT TO CONTINUE Y/N : ");
		scanf(" %c",&ch);
	}
}

































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































